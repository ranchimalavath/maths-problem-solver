# Example 1: Simple arithmetic operations
print(2 + 3)
print(4 - 1)

# Example 2: Basic conditional statements
age = 25
if age > 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# Example 3: Looping through a range of numbers
numbers = list(range(1, 10))
for num in numbers:
    if num % 2 == 0:
        print(num)
