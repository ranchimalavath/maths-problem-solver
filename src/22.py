# Example: Solve a simple linear equation: 2x + 3 = 7

# Given coefficients of x and the constant term
coefficient_x = 2
constant_term = 3
constant_value = 0 # for simplicity, we assume there is no constant term

# Calculate the coefficient of x
solution_x_coefficient = (constant_value - constant_term) / coefficient_x

print(f"The solution is: x = {solution_x_coefficient}")
