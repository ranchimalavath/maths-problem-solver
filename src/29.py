def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Args:
        a (int): The first number.
        b (int): The second number.
        
    Returns:
        int: The sum of the two numbers.
    """
    return a + b

# Example usage
result = calculate_sum(5, 3)
print("The sum is:", result)
